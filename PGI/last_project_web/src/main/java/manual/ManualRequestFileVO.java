package manual;

public class ManualRequestFileVO {
	private int req_file_id, req_board_id;
	private String  req_filename, req_filepath;

	

	public int getReq_file_id() {
		return req_file_id;
	}

	public void setReq_file_id(int req_file_id) {
		this.req_file_id = req_file_id;
	}

	public int getReq_board_id() {
		return req_board_id;
	}

	public void setReq_board_id(int req_board_id) {
		this.req_board_id = req_board_id;
	}

	public String getReq_filename() {
		return req_filename;
	}

	public void setReq_filename(String req_filename) {
		this.req_filename = req_filename;
	}

	public String getReq_filepath() {
		return req_filepath;
	}

	public void setReq_filepath(String req_filepath) {
		this.req_filepath = req_filepath;
	}
	
	
}
